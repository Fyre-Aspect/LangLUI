export interface WordMap {
  words:      string[];
  nodeMap:    Map<string, Text[]>;
  contextFor: Record<string, string>;
}

export function getSentenceContext(node: Text, word: string): string {
  const text = node.nodeValue ?? '';
  const idx  = text.toLowerCase().indexOf(word.toLowerCase());
  if (idx === -1) return '';
  const start = Math.max(0, idx - 75);
  const end   = Math.min(text.length, idx + word.length + 75);
  return text.slice(start, end).trim();
}

export const selectWords = (intensity: number): WordMap => {
  const walk = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent) return NodeFilter.FILTER_REJECT;
      const tag = parent.tagName;
      if (['SCRIPT', 'STYLE', 'NOSCRIPT', 'TEXTAREA', 'INPUT', 'CODE', 'PRE'].includes(tag)) {
        return NodeFilter.FILTER_REJECT;
      }
      // Skip already-replaced spans
      if (parent.classList?.contains('langlua-word') || parent.classList?.contains('langlua-tryout')) {
        return NodeFilter.FILTER_REJECT;
      }
      return NodeFilter.FILTER_ACCEPT;
    },
  });

  const stopwords = new Set([
    'the','and','for','with','that','this','have','will','from','they','what',
    'when','where','which','there','their','been','would','could','should','about',
    'other','into','your','more','also','some','than','then','only','very','just',
    'like','over','such','these','those','here','each','much','after','before',
    'while','being','make','take','come','know','think','look','want','give',
    'back','good','down','well','find','does','help','said','were','made',
    'even','most','used','many','long','high','both','area','need','large',
    'often','hand','home','read','next','hard','real','play','left','important',
  ]);

  const nodeMap    = new Map<string, Text[]>();
  const wordsSet   = new Set<string>();

  let node;
  while ((node = walk.nextNode())) {
    const textNode = node as Text;
    const text     = textNode.nodeValue || '';
    const tokens   = text.split(/[\s\p{P}]+/u);

    for (const word of tokens) {
      const lower = word.toLowerCase();
      if (!/^[a-zA-Z]{4,15}$/.test(word) || stopwords.has(lower)) continue;

      let prob = 0.05;
      if (intensity >= 4 && intensity <= 6) prob = 0.15;
      if (intensity >= 7) prob = 0.30;

      if (Math.random() < prob) {
        wordsSet.add(lower);
        const nodes = nodeMap.get(lower) ?? [];
        nodes.push(textNode);
        nodeMap.set(lower, nodes);
      }
    }
  }

  const words = Array.from(wordsSet);

  const contextFor: Record<string, string> = {};
  for (const word of words) {
    const firstNode = nodeMap.get(word)?.[0];
    if (firstNode) contextFor[word] = getSentenceContext(firstNode, word);
  }

  return { words, nodeMap, contextFor };
};
